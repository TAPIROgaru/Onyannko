#pragma once
#include "DxLib.h"

//---------------------------------------------------------------------------------------------
//自作ファイル

#include "Vector2.h"
#include "Color.h"

//---------------------------------------------------------------------------------------------

namespace tpr {

	//線分
	class Line {
	public:

		//-------------------------------------------------------------------------------------
		//コンストラクタ

		//=======================================================
		// 変数宣言のみ
		//=======================================================
		Line() :
			start_pos(0, 0),
			end_pos(0, 0)
		{}

		//=======================================================
		// 座標指定
		// 1. 始点
		// 2. 終点
		//=======================================================
		Line(Vector2 start_pos, Vector2 end_pos) :
			start_pos(start_pos),
			end_pos(end_pos)
		{}

		//-------------------------------------------------------------------------------------
		//オペレータ

		Line operator =(const Line line) {
			return Line(start_pos = line.start_pos, end_pos = line.end_pos);
		}


		//-------------------------------------------------------------------------------------
		//関数

		//=======================================================
		// 線の描画
		// 1. 色
		//=======================================================
		void DrawLine(unsigned int c);

		//=======================================================
		// 線の描画
		// 1. 色
		//=======================================================
		void DrawLine(Color c);

		//-------------------------------------------------------------------------------------
		//変数

		Vector2 start_pos;
		Vector2 end_pos;

	};
}