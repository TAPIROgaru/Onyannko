#pragma once
#include "DxLib.h"

//---------------------------------------------------------------------------------------------
//自作ファイル

#include "Vector2.h"


//---------------------------------------------------------------------------------------------

namespace tpr {

	//線分
	class Line {
	public:

		//-------------------------------------------------------------------------------------
		//コンストラクタ

		//=======================================================
		// 変数宣言のみ
		//=======================================================
		Line() :
			start_pos(0, 0),
			end_pos(0, 0)
		{}

		//=======================================================
		// 座標指定
		// 1. 始点
		// 2. 終点
		//=======================================================
		Line(Vector2 start_point, Vector2 end_point) :
			start_pos(start_point),
			end_pos(end_point)
		{}

		//-------------------------------------------------------------------------------------
		//オペレータ

		Line operator =(const Line line) {
			return Line(start_pos = line.start_pos, end_pos = line.end_pos);
		}


		//-------------------------------------------------------------------------------------
		//関数

		void DrawLine() {
			DxLib::DrawLine((int)start_pos.x, (int)start_pos.y, (int)end_pos.x, (int)end_pos.y, -1);
		}

		//-------------------------------------------------------------------------------------
		//変数

		Vector2 start_pos;
		Vector2 end_pos;

	};
}